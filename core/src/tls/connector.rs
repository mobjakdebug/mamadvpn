#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_rustls_connector_creation() {
        let config = TlsConnectorConfig {
            alpn: vec!["http/1.1".into()],
            verify_certs: false, // disable for test
            ..Default::default()
        };
        let connector = RustlsConnector::new(config).unwrap();
        assert_eq!(connector.config().alpn[0], "http/1.1");
    }

    #[test]
    fn test_rustls_connector_with_custom_alpn() {
        let config = TlsConnectorConfig {
            alpn: vec!["h2".into(), "http/1.1".into()],
            verify_certs: false,
            ..Default::default()
        };
        let connector = RustlsConnector::new(config).unwrap();
        assert_eq!(connector.config().alpn.len(), 2);
    }

    #[test]
    fn test_rustls_connector_with_verify_disabled() {
        let config = TlsConnectorConfig {
            verify_certs: false,
            ..Default::default()
        };
        // Should succeed even with no root certs
        let connector = RustlsConnector::new(config).unwrap();
        assert!(!connector.config().verify_certs);
    }

    #[test]
    fn test_server_name_parsing() {
        // Verify that valid domain names parse correctly
        let valid_names = ["example.com", "www.cloudflare.com", "auth.vercel.com", "xn--fsqu00a.xn--0zwm56d"];
        for name in &valid_names {
            let sn = ServerName::try_from(*name);
            assert!(sn.is_ok(), "Failed to parse valid domain: {name}");
        }
    }
}
